    private void cargarTabla() {
        modelo.setCensadors(cd.listAll());
        tblCensador.setModel(modelo);
        tblCensador.updateUI();
    }

 

    public void limpiar() {
        txtCodigo.setText(cd.generatedCode());
        txtCodigo.setEnabled(false);
        txtNombre.setText("");
        cargarTabla();
    }

    private Boolean validar() {
        return !txtNombre.getText().trim().isEmpty()
                && !txtCodigo.getText().trim().isEmpty();
    }

    public void guardar() {
        if (validar()) {
            try {

                cd.getCensador().setCodigo(txtCodigo.getText());
                cd.getCensador().setNombre(txtNombre.getText());

                if (cd.getCensador().getId() != null) {

                    if (cd.update(fila)) {
                        limpiar();
                        JOptionPane.showMessageDialog(null,
                                "Se Modifico correctamente",
                                "Mensaje",
                                JOptionPane.INFORMATION_MESSAGE);

                    } else {
                        JOptionPane.showMessageDialog(null,
                                "Error al Modificar",
                                "Error",
                                JOptionPane.ERROR_MESSAGE);
                    }

                } else {
                    if (cd.save()) {
                        limpiar();
                        JOptionPane.showMessageDialog(null,
                                "Se Guard√≥ correctamente",
                                "Mensaje",
                                JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null,
                                "Error al Guardar",
                                "Error",
                                JOptionPane.ERROR_MESSAGE);
                    }
                }
            } catch (Exception e) {
                System.out.println("Error Inesperado");
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "Llene todos los campos",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void cargarVistas() {
        int fila = tblCensador.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null,
                    "Seleccione una fila",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                this.fila = fila;
              
               cd.setCensador(modelo.getCensadors().get(fila));
               txtCodigo.setText(cd.getCensador().getCodigo());
               txtNombre.setText(cd.getCensador().getNombre());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,
                        "Seleccione una fila",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);

            }
        }
    }

